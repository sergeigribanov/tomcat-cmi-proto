CREATE TABLE experiments (
       id SERIAL,
       etag TEXT UNIQUE NOT NULL,
       CONSTRAINT etag_ct CHECK (etag ~ '^[A-Za-z0-9]+$')
);

CREATE TABLE epoints (
       id SERIAL,
       etag TEXT NOT NULL references experiments(etag),
       ptag TEXT UNIQUE NOT NULL,
       ebeam REAL,
       mfield REAL,
       CONSTRAINT ptag_ct CHECK (ptag ~ '^[0-9\.]+_\d+$')
);

CREATE TABLE simtags (
       id SERIAL,
       stag TEXT UNIQUE NOT NULL,
       CONSTRAINT stag_ct CHECK (stag ~ '^[A-Za-z0-9+\-]+$')
);

CREATE TABLE trph_sim_paths (
       id SERIAL,
       ptag TEXT NOT NULL references epoints(ptag),
       stag TEXT NOT NULL references simtags(stag),
       spath TEXT UNIQUE NOT NULL,
       UNIQUE (ptag, stag)
);

CREATE TABLE userroles (
       id SERIAL,
       userrole TEXT UNIQUE NOT NULL,
       CONSTRAINT userrole CHECK (userrole ~ '^[A-Z0-9]+$')
);

CREATE TABLE users (
       id SERIAL,
       username TEXT UNIQUE NOT NULL,
       userrole TEXT NOT NULL references userroles(userrole),
       hash TEXT NOT NULL
);

